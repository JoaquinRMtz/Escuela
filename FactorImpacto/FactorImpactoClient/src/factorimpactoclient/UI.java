    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int selectedRow = this.tblDatos.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        int res = JOptionPane.showConfirmDialog(this, "¿Seguro que queres borrar este registro?",
                 "Confirmar", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(this.tblDatos.getValueAt(selectedRow, 0).toString());
            String delete = factorImpactWSPort.delete(id);
            JOptionPane.showMessageDialog(this, delete);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.tblDatos.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        AddDialog addDialog = new AddDialog(this, true, false);
        FactorImpact factor = (FactorImpact) factorImpactWSPort.find2("{\"column\":\"id\",\"value\":\""
                + this.tblDatos.getValueAt(selectedRow, 0) + "\"}");
        //System.out.println(factor);
        if (factor != null) {
            addDialog.setUpdating(factor);
            addDialog.setVisible(true);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

