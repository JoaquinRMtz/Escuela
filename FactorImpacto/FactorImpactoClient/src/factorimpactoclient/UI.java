    private void fillTable(List<MySQLEntity> datos) {
        Integer count = factorImpactWSPort.getCount();
        this.totalPages = (count%MAX_ROWS_SHOWN == 0)? count/MAX_ROWS_SHOWN : count/MAX_ROWS_SHOWN+1;
        this.lblTotal.setText("| Un total de "+count+" registros en "+this.totalPages+" páginas.");        
        ((SpinnerNumberModel)(this.spPaginas.getModel())).setMaximum(this.totalPages);
        ((DefaultTableModel) this.tblDatos.getModel()).setRowCount(0);//clean
        for (int i = 0; i < datos.size(); i++) {
            FactorImpact get = (FactorImpact) datos.get(i);
            JcrData jcrData = get.getJCRData();
            EigenFactorMetrics eigenFactorMetrics = get.getEigenFactorMetrics();
            ((DefaultTableModel) this.tblDatos.getModel()).addRow(
                    new Object[]{
                        get.getId(),
                        get.getIssn(),
                        get.getTitle(),
                        jcrData.getTotalCities(),
                        jcrData.getFactorImpact(),
                        jcrData.getFiveImpactFactor(),
                        jcrData.getImmediacy(),
                        jcrData.getArticles(),
                        jcrData.getCitedHalfLife(),
                        eigenFactorMetrics.getEigenFactorScore(),
                        eigenFactorMetrics.getArticleInfluenceScore()
                    }
            );
        }
    }
